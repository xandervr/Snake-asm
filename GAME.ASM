PROC spawnWalls
	USES eax
	LOCAL @@counter

	mov eax, 4
	@@loop:
		cmp eax, 9
		je @@next
		call addWall, 4, eax
		inc eax
		jmp @@loop
	@@next:
		mov eax, 11
	@@loop1:
		cmp eax, 20
		je @@next1
		call addWall, 4, eax
		inc eax
		jmp @@loop1
	@@next1:
		mov eax, 10
  @@loop2:
		cmp eax, 15
		je @@next2
		call addWall, eax, 22
		inc eax
		jmp @@loop2
	@@next2:
		mov eax, 18
	@@loop3:
		cmp eax, 36
		je @@next3
		call addWall, eax, 22
		add eax, 2
		jmp @@loop3
	@@next3:

		ret
ENDP spawnWalls

PROC addWall
	ARG @@x	:dword, \
			@@y	:dword
	USES eax, ebx

	mov eax, [WALLS_ARRAY_LEN]
	mov ebx, [@@x]
	imul ebx, [TILE_SIZE]
	mov [WALLS_ARRAY_X + 4 * eax], ebx

	mov ebx, [@@y]
	imul ebx, [TILE_SIZE]
	mov [WALLS_ARRAY_Y + 4 * eax], ebx

	inc eax
	mov [WALLS_ARRAY_LEN], eax

	ret
ENDP addWall

PROC drawWalls
	USES eax, ebx, ecx

	mov eax, 0
	@@loop:
		cmp eax, [WALLS_ARRAY_LEN]
		je  @@loopEnd
		mov ebx, [WALLS_ARRAY_X + 4*eax]
		mov ecx, [WALLS_ARRAY_Y + 4*eax]
		call drawSprite, offset _wall, ebx, ecx
		inc eax
		jmp @@loop
	@@loopEnd:

		ret
ENDP

PROC spawnRandomApple
	USES eax, ebx, ecx

	call rand
	mov edx, 0
	mov ebx, [GRID_WIDTH]
	div ebx
	imul edx, [TILE_SIZE]
	mov [APPLE_X], edx

	call rand
	mov edx, 0
	mov ebx, [GRID_HEIGHT]
	div ebx
	imul edx, [TILE_SIZE]
	mov [APPLE_Y], edx

	mov ebx, [APPLE_Y]
	cmp ebx, 0
	jne @@end
	call spawnRandomApple ; Check if apple is on Y 0
	@@end:

	; Check if apple is inside walls
	mov eax, 0
	@@loopWall:
		cmp eax, [WALLS_ARRAY_LEN]
		je  @@loopWallEnd
		mov ebx, [WALLS_ARRAY_X + 4*eax]
		mov ecx, [WALLS_ARRAY_Y + 4*eax]
		cmp ebx, [APPLE_X]
		jne @@noWallHit
		cmp ecx, [APPLE_Y]
		jne @@noWallHit
		call spawnRandomApple
		@@noWallHit:
		inc eax
		jmp @@loopWall
	@@loopWallEnd:

	; Check if apple is inside snake
	mov eax, 1
	@@loop:
		cmp eax, [SNAKE_ARRAY_LEN]
		je  @@loopEnd
		mov ebx, [SNAKE_ARRAY_X + 4*eax]
		mov ecx, [SNAKE_ARRAY_Y + 4*eax]
		cmp ebx, [SNAKE_ARRAY_X]
		jne @@notHit
		cmp ecx, [SNAKE_ARRAY_Y]
		jne @@notHit
		call spawnRandomApple
		@@notHit:
		inc eax
		jmp @@loop
	@@loopEnd:

	ret
ENDP spawnRandomApple

PROC drawApple

	call drawSprite, offset _apple, [APPLE_X], [APPLE_Y]
	ret
ENDP drawApple

PROC addSegment
	USES eax, ebx, ecx

  mov eax, [SNAKE_ARRAY_LEN]
	mov ebx, [SNAKE_ARRAY_X+4*eax]
	mov ecx, [SNAKE_ARRAY_Y+4*eax]
	add eax, 1
	mov [SNAKE_ARRAY_X+4*eax], ebx
	mov [SNAKE_ARRAY_Y+4*eax], ecx
	mov [SNAKE_ARRAY_LEN], eax

	ret
ENDP addSegment

PROC clearSnakeSegment
	USES eax
	mov eax, [SNAKE_ARRAY_LEN]
	dec eax
	call drawSprite, offset _black, [SNAKE_ARRAY_X + eax * 4], [SNAKE_ARRAY_Y + eax * 4]

	ret
ENDP clearSnakeSegment


PROC drawSnake
	LOCAL @@counter	:dword
	USES eax, ebx, ecx, edx

	mov ebx, [SNAKE_ARRAY_X]
	add ebx, [DIRECTION_X] ; ebx = new X
	mov ecx, [SNAKE_ARRAY_Y]
	add ecx, [DIRECTION_Y] ; ecx = new Y

	; collisions
	@@left_wall_hit:
		mov eax, 0
		sub eax, [TILE_SIZE]
		cmp ebx, eax
		jne @@right_wall_hit
		mov eax, [SCREEN_WIDTH]
		sub eax, [TILE_SIZE]
		mov ebx, eax
	@@right_wall_hit:
		mov edx, [SCREEN_WIDTH]
		cmp ebx, edx
		jne @@upper_wall_hit
		mov ebx, 0
	@@upper_wall_hit:
		mov eax, 0
		cmp ecx, eax
		jne @@lower_wall_hit
		mov ecx, [SCREEN_HEIGHT]
	@@lower_wall_hit:
		mov edx, [SCREEN_HEIGHT]
		add edx, [TILE_SIZE]
		cmp ecx, edx
		jne @@end
		mov ecx, 0
	@@end:
		call clearSnakeSegment
	 	call drawSprite, offset _snakeSegment, ebx, ecx

	mov eax, 0
	;;;; loopen
	@@loop:
		cmp eax, [SNAKE_ARRAY_LEN]
		je  @@loopEnd
		mov edx, [SNAKE_ARRAY_X + 4*eax]
		mov [SNAKE_ARRAY_X + 4*eax], ebx
		mov ebx, edx
		mov edx, [SNAKE_ARRAY_Y + 4*eax]
		mov [SNAKE_ARRAY_Y + 4*eax], ecx
		mov ecx, edx
		inc eax
		jmp @@loop
	@@loopEnd:
	; TODO

			ret
ENDP drawSnake


; MAIN Start of program
start:
  sti                             ; Set The Interrupt Flag
  cld                             ; Clear The Direction Flag

  push ds 						; Put value of DS register on the stack
  pop es 							; And write this value to ES

	; Setup and initialization
	call setupVideo
	call initGameState

	; Main game (endless) loop

	mov ebx, 0

	@@gameLoop:
	call handleUserInput

	cmp [GAME_STARTED], 1
	je @@noMenu
	cmp ebx, 40
	jne @@noClear
	call clearSpaceText
	mov ebx, 40
	@@waitLoop:
		cmp ebx, 0
		je @@gameLoop
		call drawAll
		dec ebx
		jmp @@waitLoop
	@@noClear:
	call drawAll
	call drawMenu
	inc ebx
	jmp @@gameLoop
	@@noMenu:
	cmp [GAME_OVER], 1
	jne @@noEnd
	call drawAll
	call drawEnd

	jmp @@gameLoop
	@@noEnd:

	call drawAll
	call drawApple
	call drawScore

	call drawWalls

	mov eax, [gameLoopCounter]
	cmp eax, 10
	jl @@move_over
	call updateGameState
	call drawSnake
	mov [gameLoopCounter], 0
	@@move_over:
	call updateGameState

	jmp @@gameLoop



