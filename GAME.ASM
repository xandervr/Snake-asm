PROC drawScore
	USES eax, ebx, ecx, edx
	LOCAL @@x :dword, \
				@@y :dword

	mov eax, [SCORE]
	mov edx, 0
	mov ebx, 10
	div ebx

	mov ecx, 0

	@@loop:

		mov ebx, [TILE_SIZE]
		imul ecx, ebx
		mov ebx, [SCORE_X]
		imul ebx, [TILE_SIZE]
		sub ebx, ecx
		mov [@@x], ebx
		mov ebx, [SCORE_Y]
		imul ebx, [TILE_SIZE]
		mov [@@y], ebx


		@@isZero:
			cmp edx, 0
			jne @@isOne
			call drawSprite, offset _digitZero, [@@x], [@@y]
			jmp @@noDigit
		@@isOne:
			cmp edx, 1
			jne @@isTwo
			call drawSprite, offset _digitOne, [@@x], [@@y]
			jmp @@noDigit
		@@isTwo:
			cmp edx, 2
			jne @@isThree
			call drawSprite, offset _digitTwo, [@@x], [@@y]
			jmp @@noDigit
		@@isThree:
			cmp edx, 3
			jne @@isFour
			call drawSprite, offset _digitThree, [@@x], [@@y]
			jmp @@noDigit
		@@isFour:
			cmp edx, 4
			jne @@isFive
			call drawSprite, offset _digitFour, [@@x], [@@y]
			jmp @@noDigit
		@@isFive:
			cmp edx, 5
			jne @@isSix
			call drawSprite, offset _digitFive, [@@x], [@@y]
			jmp @@noDigit
		@@isSix:
			cmp edx, 6
			jne @@isSeven
			call drawSprite, offset _digitSix, [@@x], [@@y]
			jmp @@noDigit
		@@isSeven:
			cmp edx, 7
			jne @@isEight
			call drawSprite, offset _digitSeven, [@@x], [@@y]
			jmp @@noDigit
		@@isEight:
			cmp edx, 8
			jne @@isNine
			call drawSprite, offset _digitEight, [@@x], [@@y]
			jmp @@noDigit
		@@isNine:
			cmp edx, 9
			jne @@noDigit
			call drawSprite, offset _digitNine, [@@x], [@@y]
			jmp @@noDigit
		@@noDigit:
			inc ecx

			cmp eax, 0
			je @@endLoop

			mov edx, 0
			mov ebx, 10
			div ebx
			jmp @@loop
		@@endLoop:

	ret
ENDP drawScore

