; Draw Sprites
PROC drawSprite
	ARG @@spritePtr :dword, \
			@@x:dword, \
			@@y:dword
	LOCAL @@w:dword, @@h:dword
	USES eax , ebx , ecx , edx , esi , edi

	mov      esi , [ @@spritePtr ]
	xor      eax , eax
	lodsw    ; read width in AX
	mov      [@@w] , eax
	lodsw    ; read height in AX
	mov      [@@h] , eax
	mov      edi , offset videoBuffer
	mov      eax , [@@y]
	mov      ebx , [SCREEN_WIDTH]
	mul      ebx
	add      edi, eax
	add      edi, [@@x]   ; EDI points to forst pixel
	mov      ecx, [@@h]
@@drawLine:
	push     ecx
	mov      ecx, [@@w]    ; need to copy a line of the sprite
	rep      movsb         ; copy from esi to edi
	add      edi, [SCREEN_WIDTH]
	sub      edi, [@@w]    ; edit now points to the next line in dst
	pop      ecx
	dec      ecx
	jnz      @@drawLine

	ret
ENDP drawSprite

; Procedure to handle user input
PROC handleUserInput
	USES eax

	mov ah, 01h ; function 01h (test key pressed)
	int 16h		; call keyboard BIOS
	jz @@no_key_pressed
	mov ah, 00h
	int 16h

	; process key code here (scancode in AH, ascii code in AL)
	cmp ah, 01	; scancode for ESCAPE key
	jne	@@SpaceKey
	call exit
	jmp @@no_key_pressed

@@SpaceKey:
	cmp al, 32 ; Space keycode
	jne @@RightKey
	cmp [GAME_OVER], 1
	jne @@beginGame

	mov [GAME_STARTED], 0
	mov [GAME_OVER], 0
	call clearScreen
	call initGameState
	jmp @@no_key_pressed

	@@beginGame:
	call beginGame
	call clearScreen
	jmp @@no_key_pressed

@@RightKey:
	cmp ah, 77	; arrow right
	jne @@LeftKey

	; Check if snake is longer than 1
	mov eax, [SNAKE_ARRAY_LEN]
	cmp eax, 1
	je @@noLeftCheck
	; Check if snake is going left
	mov eax, 0
	sub eax, [TILE_SIZE]
	cmp eax, [DIRECTION_X]
	je @@no_key_pressed
	@@noLeftCheck:
	mov eax, 0
	add eax, [TILE_SIZE]
	mov [DIRECTION_X], eax
	mov [DIRECTION_Y], 0

	jmp @@no_key_pressed

@@LeftKey:
	cmp ah, 75	; arrow left
	jne @@DownKey
	; Check if snake is longer than 1
	mov eax, [SNAKE_ARRAY_LEN]
	cmp eax, 1
	je @@noRightCheck
	; Check if snake is going right
	mov eax, [TILE_SIZE]
	cmp eax, [DIRECTION_X]
	je @@no_key_pressed
	@@noRightCheck:
	mov eax, 0
	sub eax, [TILE_SIZE]
	mov [DIRECTION_X], eax
	mov [DIRECTION_Y], 0
	jmp @@no_key_pressed

@@DownKey:
	cmp ah, 80	; arrow down
	jne @@UpKey
	; Check if snake is longer than 1
	mov eax, [SNAKE_ARRAY_LEN]
	cmp eax, 1
	je @@noUpCheck
	; Check if snake is going up
	mov eax, 0
	sub eax, [TILE_SIZE]
	cmp eax, [DIRECTION_Y]
	je @@no_key_pressed
	@@noUpCheck:
	mov [DIRECTION_X], 0
	mov eax, 0
	add eax, [TILE_SIZE]
	mov [DIRECTION_Y], eax
	jmp @@no_key_pressed

@@UpKey:
	cmp ah, 72	; arrow up
	jne @@NoKey
	; Check if snake is longer than 1
	mov eax, [SNAKE_ARRAY_LEN]
	cmp eax, 1
	je @@noDownCheck
	; Check if snake is going down
	mov eax, [TILE_SIZE]
	cmp eax, [DIRECTION_Y]
	je @@no_key_pressed
	@@noDownCheck:
	mov [DIRECTION_X], 0
	mov eax, 0
	sub eax, [TILE_SIZE]
	mov [DIRECTION_Y], eax
	jmp @@no_key_pressed

@@NoKey:
@@no_key_pressed:
	ret
ENDP handleUserInput

; Procedure to initialize a new game
PROC initGameState
	call rand_init
	mov [SNAKE_ARRAY_X], 0
	mov eax, [TILE_SIZE]
	mov [SNAKE_ARRAY_Y], eax
	mov [SNAKE_ARRAY_LEN], 1
	mov [DIRECTION_X], eax
	mov [DIRECTION_Y], 0
	mov [SCORE], 0
	mov [WALLS_ARRAY_LEN], 0
	call spawnRandomApple
	call spawnWalls
	ret
ENDP initGameState

; Procedure to update the game world status (like enemies, collisions, events, ...)
PROC updateGameState
	USES eax, ebx, ecx, edx

	; Check if snake hits apple
	mov eax, [SNAKE_ARRAY_X]
	mov ebx, [SNAKE_ARRAY_Y]
	cmp eax, [APPLE_X]
	jne @@noAppleHit
	cmp ebx, [APPLE_Y]
	jne @@noAppleHit
	inc [SCORE]
	call addSegment
	call spawnRandomApple
	@@noAppleHit:
	; Check if snake hits itself
	mov eax, 1
	@@loop:
		cmp eax, [SNAKE_ARRAY_LEN]
		je  @@loopEnd
		mov ebx, [SNAKE_ARRAY_X + 4*eax]
		mov ecx, [SNAKE_ARRAY_Y + 4*eax]
		cmp ebx, [SNAKE_ARRAY_X]
		jne @@notHit
		cmp ecx, [SNAKE_ARRAY_Y]
		jne @@notHit
		mov [GAME_OVER], 1
		@@notHit:
		inc eax
		jmp @@loop
	@@loopEnd:

	; Check if snake hits wall
	mov eax, 0
	@@loopWall:
		cmp eax, [WALLS_ARRAY_LEN]
		je  @@loopWallEnd
		mov ebx, [WALLS_ARRAY_X + 4*eax]
		mov ecx, [WALLS_ARRAY_Y + 4*eax]
		cmp ebx, [SNAKE_ARRAY_X]
		jne @@noWallHit
		cmp ecx, [SNAKE_ARRAY_Y]
		jne @@noWallHit
		mov [GAME_OVER], 1
		@@noWallHit:
		inc eax
		jmp @@loopWall
	@@loopWallEnd:


	inc [gameLoopCounter]
	ret
ENDP updateGameState

