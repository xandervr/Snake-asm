PROC clearScreen
	USES eax

	xor eax, eax

	@@loop:
		cmp eax, 64000
		je @@end
		mov [videoBuffer+eax], 0
		inc eax
		jmp @@loop
	@@end:
	ret
ENDP clearScreen

